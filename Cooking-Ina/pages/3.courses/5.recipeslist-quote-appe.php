                            <h2>Directions</h2>

                            <ol>
                                <li>Prepare the ground pork stuffing by heating 3 tablespoons oil in a pan. Saute garlic
                                    and onion until the latter softens. Add ground pork. Saute until medium brown.
                                    Season with salt and ground black pepper. Remove from the pan and put on a large
                                    bowl. Let it cool down. Beat 1 piece of egg and pour into the cooked grounc pork.
                                    Mix well. Set aside.</li>
                                <li>Slice one side of the peppers lengthwise all the way to the bottom. Remove the seeds
                                    by gently scraping using a small spoon or a butter knife. Set aside.</li>
                                <li>Slice the cheddar cheese into long pieces. Stuff individual slices of cheese into
                                    each pepper. Sccop the cooked meat mixture and stuff into the chili peppers. Make
                                    sure that there is enough meat.</li>
                                <li>Wrap the stuffed peppers in lumpia wrapper.</li>
                                <li>Heat 2 cups of oil in a pan. Fry each piece of dynamite lumpia in medium heat for 2
                                    minutes per side or until lumpia wrapper turns golden brown. Note: you may fry
                                    longer if needed.</li>
                                <li>Remove from pan and place over a wire rack. Let it cool down Arrange in a serving
                                    plate and then serve with your favorite condiment.</li>
                                <li>Share and enjoy!</li>
                            </ol>


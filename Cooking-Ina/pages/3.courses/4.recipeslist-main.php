                    <a href="#recipes-main" class="close">&times;</a>
                    <h2>Teriyaki Salmon </h2>
                    <div class="containers">
                        <div class="top">
                            <h2>Ingredients</h2>
                            <table>
                                <tr>
                                    <th> Name </th>

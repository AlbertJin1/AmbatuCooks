                                </tr>

                                <tr>
                                    <td> Short-grain rice </td>
                                    <td> 1 cup </td>
                                </tr>

                                <tr>
                                    <td> Water </td>
                                    <td> 3 cups </td>
                                </tr>

                                <tr>
                                    <td> koji</td>
